# Multiple Assignment --> allows us to assign multiple variables at the same time in one line of code

# Typical Variable Assignment
name = "Jesus"
age = 21
human = True
print(name)
print(age)
print(human)

# Multiple Assignment
full_name, age, attractive = "Osamu Dazai", 25, True
print(full_name , age , attractive)

# Also, if many variables have same values, then multiple assignment can be used as well.
# for e.g.
# Trevor = 29
